public class Club {
    private String ma, ten;
    private int giave;
    public Club (String ma, String ten, int giave) {
        this.ma = ma;
        this.ten = ten;
        this.giave = giave;
    }
    public String getMa() {
        return ma;
    }
    public String getTen() {
        return ten;
    }
    public int getGiave() {
        return giave;
    }
}
