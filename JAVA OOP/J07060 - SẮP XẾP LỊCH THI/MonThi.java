public class MonThi {
    private String maMon, tenMon, hinhthucthi;
    public MonThi (String maMon, String tenMon, String hinhthucthi) {
        this.maMon = maMon;
        this.tenMon = tenMon;
        this.hinhthucthi = hinhthucthi;
    }
    public String getMaMon() {
        return maMon;
    }
    public String getTenMon() {
        return tenMon;
    }
}
