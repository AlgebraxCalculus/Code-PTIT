public class MonHoc {
    private String ma, ten;
    private int so_tc;
    public MonHoc (String ma, String ten, int so_tc) {
        this.ma = ma;
        this.ten = ten;
        this.so_tc = so_tc;
    }
    public String getMa() {
        return ma;
    }
    public String getTen() {
        return ten;
    }
}
